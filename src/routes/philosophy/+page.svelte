    <h1 class="text-3xl font-bold mt-4 mb-4">PHILOSOPHY OF “REALISM AND PRACTICALISM” - BY ALTAF HUSSAIN</h1>
    <p>The philosophy of Realism and Practicalism is a criterion for checking the righteousness, evilness, wrongness,
  truthfulness and falseness of everything. This includes philosophies, ideologies, theories, policies, concepts, systems
  including medieval, modern and prevailing system of governing a country or institutions.</p>
    <p>It is also helpful for checking the truthfulness, concoctions, lies of statements, analytical write-ups, columns, versions
  and perceptions either generated, created, emanated or given by different quarters, elements, institutions or individuals.</p>
  
    <h2 class="text-2xl font-bold mt-4 mb-2">What is Realism?</h2>
    <p>Realism is an act of accepting reality in its actual spirit and essence including facts, physical universe, events,
  necessities of life, ground realities, favourable or unfavourable situations, conditions and atmosphere, as they are, as
  opposed to the abstract or ideal.</p>
    <p>In this regard a practical approach be adopted rather than having ideal, moral, religious or romantic approach. It does
  not matter whether one likes or dislikes things as they are or appear to be rather than as one might wish them to be. For
  example, Sun rises from the east whether one likes or dislikes the phenomenon.</p>
  
    <h2 class="text-2xl font-bold mt-4 mb-2">What is Practicalism?</h2>
    <p>An act of doing things, making or taking decisions, adopting means and ways in accordance with the actual spirit and
  essence of reality (realities) and realism is called Practicalism.</p>
    <p>For example when one sees and observes that there are lackings and shortcomings in any prevailing system and one thinks
  that positive steps and corrective measures should be taken to make the system better and beneficial for the people at large.
  To obtain better and beneficial results, one has to take positive steps and corrective measures through committed,
  well-determined manner and hard work.</p>
  
